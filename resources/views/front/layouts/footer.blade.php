<script type="text/javascript">

    $('#contactForm').on('submit',function(e){
        e.preventDefault();

        $('#citie_id-error').text('');
        $('#area_id-error').text('');
        $('#address-error').text('');

        let service_id = $('#service_id').val();
        let artist_id = $('#artist_id').val();
        let user_id = $('#user_id').val();
        let citie_id = $('#citie_id').val();
        let area_id = $('#area_id').val();
        let address = $('#address').val();

        $.ajax({
            url: "{{URL::to('/contact-form')}}",
            type:"POST",
            data:{
                "_token": "{{ csrf_token() }}",
                service_id:service_id,
                artist_id:artist_id,
                user_id:user_id,
                citie_id:citie_id,
                area_id:area_id,
                address:address
            },
            success:function(response){
                console.log(response);
                if (response) {
                    $('#success-message').text(response.success);
                    $("#contact-form")[0].reset();
                }
            },
            error: function(response) {
                $('#citie_id-error').text(response.responseJSON.errors.citie_id);
                $('#area_id-error').text(response.responseJSON.errors.area_id);
                $('#address-error').text(response.responseJSON.errors.address);
            },


        });
    });
    $('#hidden').on('click', function () {
        $('#contactForm').hide();
    });
</script>
